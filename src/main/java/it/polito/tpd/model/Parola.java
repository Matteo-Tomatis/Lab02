package it.polito.tpd.model;

public class Parola {
	
	private String parolaUmana;
	private String parolaAliena;
	
	public Parola(String parolaUmana, String parolaAliena) {
			this.parolaUmana = parolaUmana;
			this.parolaAliena = parolaAliena;
	}
	

	public String getParolaUmana() {
		return parolaUmana;
	}

	public void setParolaUmana(String parolaUmana) {
		this.parolaUmana = parolaUmana;
	}

	public String getParolaAliena() {
		return parolaAliena;
	}

	public void setParolaAliena(String parolaAliena) {
		this.parolaAliena = parolaAliena;
	}
	
	
	

}
